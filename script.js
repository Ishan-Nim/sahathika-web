/* ─── Card art (grid) ──────────────────────────────────────────────────────── */
function renderCardArt(c) {
  /* INE portrait cards already show cert paper + badge combined — use certImg */
  if (c.type === "certificate") {
    return `<div class="credential-art credential-art--image">
      <img src="${c.certImg}" alt="${c.shortCode} credential card" loading="lazy">
    </div>`;
  }
  /* OSCP / CEH — landscape cert, show badge in card instead */
  return `<div class="credential-art credential-art--image credential-art--badge-only">
    <img src="${c.badgeImg}" alt="${c.shortCode} badge" loading="lazy">
  </div>`;
}

/* ─── Detail preview — cert state ─────────────────────────────────────────── */
function renderDetailCard(c) {
  if (c.type === "oscp") {
    return `<div class="credential-art credential-art--landscape-cert">
      <img src="${c.certImg}" alt="${c.title} certificate">
    </div>`;
  }
  if (c.type === "ceh") {
    return `<div class="credential-art credential-art--landscape-cert">
      <img src="${c.certImg}" alt="CEH certificate">
    </div>`;
  }
  /* INE — show the full portrait credential card image */
  return `<div class="credential-art credential-art--image credential-art--cert-portrait">
    <img src="${c.certImg}" alt="${c.shortCode} credential card">
  </div>`;
}

/* ─── Detail preview — badge state ────────────────────────────────────────── */
function renderDetailBadge(c) {
  return `<div class="credential-art credential-art--image credential-art--badge-view">
    <img src="${c.badgeImg}" alt="${c.shortCode} badge">
  </div>`;
}

/* ─── Grid ────────────────────────────────────────────────────────────────── */
function renderGrid() {
  credentialGrid.innerHTML = credentials.map((c) => `
    <button class="credential-card" type="button" data-id="${c.id}">
      <div class="credential-card__media">${renderCardArt(c)}</div>
      <div class="credential-card__body">
        <h3 class="credential-card__title">${c.title}</h3>
        <p class="credential-card__date">${c.issuedOn}</p>
        <p class="credential-card__issuer">${c.issuer}</p>
      </div>
    </button>`).join("");

  credentialGrid.querySelectorAll(".credential-card").forEach((card) => {
    card.addEventListener("click", () => showDetail(card.dataset.id));
  });
}

/* ─── Detail gallery (cert + badge thumbs) ─────────────────────────────────── */
function certThumbHtml(c) {
  /* Thumb 1 always shows the certificate image, never the badge */
  if (c.type === "certificate") {
    return `<div class="credential-art credential-art--image">
      <img src="${c.certImg}" alt="${c.shortCode} cert" loading="lazy">
    </div>`;
  }
  /* OSCP / CEH — landscape cert as thumb */
  return `<div class="credential-art credential-art--image credential-art--badge-only">
    <img src="${c.certImg}" alt="${c.shortCode} cert" loading="lazy">
  </div>`;
}

function updateDetailGallery(c) {
  detailPreview.innerHTML = detailGalleryState === "card"
    ? renderDetailCard(c)
    : renderDetailBadge(c);

  detailThumbs.innerHTML = "";

  [
    { state: "card",  html: certThumbHtml(c) },
    { state: "badge", html: `<div class="credential-art credential-art--image"><img src="${c.badgeImg}" alt="${c.shortCode} badge" loading="lazy"></div>` },
  ].forEach(({ state, html }) => {
    const btn = document.createElement("button");
    btn.type = "button";
    btn.className = `thumb-card${detailGalleryState === state ? " is-active" : ""}`;
    btn.innerHTML = html;
    btn.addEventListener("click", () => {
      detailGalleryState = state;
      updateDetailGallery(c);
    });
    detailThumbs.appendChild(btn);
  });
}

/* ─── Detail view ────────────────────────────────────────────────────────── */
function renderDetail(id) {
  const c = credentials.find((x) => x.id === id) ?? credentials[0];
  activeCredentialId = c.id;
  detailGalleryState = "card";

  detailTitle.textContent               = c.title;
  detailDescription.textContent         = c.summary;
  detailIssuedOn.textContent            = c.issuedOn;
  detailExpiresOn.textContent           = c.expiresOn;
  detailTalkingPoint.textContent        = c.talkingPoint;
  detailIssuerLink.textContent          = c.issuer;
  detailIssuerNameSecondary.textContent = c.issuer;
  detailIssuerBadge.innerHTML           = `<span>${c.issuerShort}</span>`;
  detailIssuerBadgeSecondary.innerHTML  = `<span>${c.issuerShort}</span>`;
  detailSkills.innerHTML                = c.skills.map((s) => `<span>${s}</span>`).join("");
  detailRecipientName.textContent       = c.recipient;
  detailRecipientAvatar.innerHTML       = `<img src="${c.recipientPhoto}" alt="${c.recipient}" onerror="this.parentElement.textContent='${c.recipientInitials}'">`;

  /* Issuer logo + verified checkmark */
  const logoSrc = ISSUER_LOGOS[c.issuer];
  const verifiedSvg = `<span class="verified-check" aria-label="Verified issuer">
    <svg viewBox="0 0 16 16"><circle cx="8" cy="8" r="8" fill="#1d9bf0"/><path d="M4.5 8l2.5 2.5 5-5" stroke="white" stroke-width="1.7" stroke-linecap="round" stroke-linejoin="round" fill="none"/></svg>
  </span>`;
  /* OffSec SVG is 529×128 — icon is at the far left, so anchor there */
  const imgPos = c.issuer === "OffSec" ? ' style="object-position:left center"' : "";
  const issuerBadgeHtml = logoSrc
    ? `<span class="issuer-badge-img"><img src="${logoSrc}" alt="${c.issuer} logo"${imgPos}></span>${verifiedSvg}`
    : `<span>${c.issuerShort}</span>`;
  detailIssuerBadge.innerHTML          = issuerBadgeHtml;
  detailIssuerBadgeSecondary.innerHTML = issuerBadgeHtml;

  /* Talking point with label */
  detailTalkingPoint.innerHTML = `<div class="value-panel__quote-label">TALKING POINT:</div>"${c.talkingPoint}"`;

  /* View All Statements */
  const viewAllBtn = document.getElementById("viewAllStatementsBtn");
  if (viewAllBtn) {
    if (c.careerJourneyUrl) {
      viewAllBtn.href = c.careerJourneyUrl;
      viewAllBtn.setAttribute("target", "_blank");
    } else {
      viewAllBtn.href = "#";
      viewAllBtn.removeAttribute("target");
    }
  }

  if (issuerWebsiteLink) issuerWebsiteLink.href = ISSUER_URLS[c.issuer] || "#";

  /* Download dropdown links */
  const certFile  = c.certImg;
  const badgeFile = c.badgeImg;
  const certName  = c.shortCode + "-certificate.png";
  const badgeName = c.shortCode + "-badge.png";
  ["dlCertOB", "dlCertW3C"].forEach((id) => {
    const el = document.getElementById(id);
    if (el) { el.href = certFile; el.setAttribute("download", certName); }
  });
  ["dlBadgeOB", "dlBadgeW3C"].forEach((id) => {
    const el = document.getElementById(id);
    if (el) { el.href = badgeFile; el.setAttribute("download", badgeName); }
  });

  /* More dropdown */
  const contactBtn = document.getElementById("contactIssuerBtn");
  if (contactBtn) contactBtn.href = `mailto:support@${c.issuer.toLowerCase().replace(/[^a-z]/g, "")}.com`;
  const moreInfoBtn2 = document.getElementById("moreInfoBtn");
  if (moreInfoBtn2) moreInfoBtn2.href = c.credentialUrl;

  const verifyBtn = document.getElementById("verifyCredentialBtn");
  if (verifyBtn) verifyBtn.onclick = () => openVerifyModal(c);

  updateDetailGallery(c);
}

/* ─── View switching ─────────────────────────────────────────────────────── */
function showDetail(id) {
  listView.classList.add("hidden");
  gridView.classList.add("hidden");
  detailView.classList.remove("hidden");
  renderDetail(id);
  window.scrollTo({ top: 0, behavior: "smooth" });
}

function showList() {
  detailView.classList.add("hidden");
  listView.classList.remove("hidden");
  gridView.classList.remove("hidden");
  window.scrollTo({ top: 0, behavior: "smooth" });
}

