/* ─── Dropdown toggles ───────────────────────────────────────────────────── */
function toggleDrop(menuId, e) {
  e && e.stopPropagation();
  const all = document.querySelectorAll(".action-drop__menu");
  const target = document.getElementById(menuId);
  const wasHidden = target.classList.contains("hidden");
  all.forEach((m) => m.classList.add("hidden"));
  if (wasHidden) target.classList.remove("hidden");
}

document.addEventListener("click", () => {
  document.querySelectorAll(".action-drop__menu").forEach((m) => m.classList.add("hidden"));
});

